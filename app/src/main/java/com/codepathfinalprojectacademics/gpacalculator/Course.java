package com.codepathfinalprojectacademics.gpacalculator;

public class Course {
    private String courseName;
    private float grade;
    private int credits;
    private String weight;

    public String getCourseName() {
        return courseName;
    }

    public float getGrade() {
        return grade;
    }

    public int getCredits() {
        return credits;
    }

    public String getWeight() {
        return weight;
    }
}
